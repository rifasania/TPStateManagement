class StoreModel {
  final String id;
  final String title;
  final String price;
  final String description;
  final String category;
  final String image;
  final String rating;
  final String rate;
  final int count;

  StoreModel({
    required this.id,
    required this.title,
    required this.price,
    required this.description,
    required this.category,
    required this.image,
    required this.rating,
    required this.rate,
    required this.count,
  });
}
